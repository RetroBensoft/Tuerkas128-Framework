;
; Blocks data
;
; Each block is composed by:
;
;	* 1 byte = Block properties in the format WWHHSSSZ
;		WW 	= Width - 1 (columns). Range 1 to 4
;       HH 	= Height - 1 (rows). Range 1 to 4
;		SSS = Hardness: 111 - Merged block   100 - Ladder   011 - Death   010 - Floor/Solid   001 - Platform   000 - Empty
;       Z 	= Depth: 1 Foreground / 0 Background
;   * (WW+1) x (HH+1) x 8 bytes 	= Block data. Top to bottom, then left to right
;	* (WW+1) x (HH+1)     bytes		= Block attributes. Top to bottom, then left to right
;
; If SSS = 111 (merged block), block data is merged with screen by ANDind both values. It can be used to blend blocks in backlighted screens
; If SSS = 111 (merged block), hardness and depth data is no modified.
;
; Every bock can be built by using the M_SINGLEBLOCK_BEGIN and M_SINGLEBLOCK_END macros
;
; For example, asuming BLOCK_COUNTER=0:
;    M_SINGLEBLOCK_BEGIN "VacioBlanco1x1", T128_BLOCK_WIDTH_1 + T128_BLOCK_HEIGHT_1
;	    				 defb	0, 0, 0, 0, 0, 0, 0, 0
;					     defb	T128_BLACK_WHITE
;	M_SINGLEBLOCK_END	 "VacioBlanco1x1"
;
; will be expanded to:
; 	BLK_VacioBlanco1x1: defb T128_BLOCK_WIDTH_1 + T128_BLOCK_HEIGHT_1
;	   				    defb 0, 0, 0, 0, 0, 0, 0, 0
;					    defb T128_BLACK_WHITE
;   T128_B_VacioBlanco1x1 EQU 0
;   PUBLIC T128_B_VacioBlanco1x1
;
; T128_B_VacioBlanco1x1 can be used as an index to reference the block at tuerkas128_scr_superblocks.asm and tuerkas128_screen_main.asm
; 
; BLK_VacioBlanco1x1 must be referred at the end of this file, in the lookup table for block pointers using the M_BLOCK_TABLE macro
;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;


